function MatchingDaysLogic(){

    var firstDay = 0
    var secondDay = 0

    function setFirstDay(num){
        firstDay = num
    }
    
    function setSecondDay(num){
        secondDay
    }

    function getSetDays(){
        return {
            secondDay,
            firstDay
        }
    }

    return {
        getSetDays,
        setFirstDay,
        setSecondDay
    }
}